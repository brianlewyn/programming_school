// Hacer un programa que muestre en la pantalla del 5 al 20 de uno en uno.
// Entrada: --
// Salida: Números del 5 al 20

#include <iostream>
using namespace std;

int main() {
   cout<<"Programa que muestra los números del 5 al 20 de uno en uno\n";

   for (int i=5; i<=20; i++) {
      cout<<"El valor es "<<i<<endl;
   }

   return 0;
}
